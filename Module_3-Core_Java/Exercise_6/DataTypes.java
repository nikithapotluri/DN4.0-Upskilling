public class DataTypes {
    public static void main(String[] args) {
        int i = 55;
        float f = 6.25f;
        double d = 3.141597;
        char c = 'N';
        boolean b = true;

        System.out.println("int: " + i);
        System.out.println("float: " + f);
        System.out.println("double: " + d);
        System.out.println("char: " + c);
        System.out.println("boolean: " + b);
    }
}
